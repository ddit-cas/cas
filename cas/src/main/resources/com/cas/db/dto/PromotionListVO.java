package com.cas.db.dto;

public class PromotionListVO {
	private String contentNum;
	private String contentTitle;
	private String genreName;
	private String startDate;
	private String endDate;
	private String contentWriter;
	private String recomCount;
	
	public String getContentNum() {
		return contentNum;
	}
	public void setContentNum(String contentNum) {
		this.contentNum = contentNum;
	}
	public String getContentTitle() {
		return contentTitle;
	}
	public void setContentTitle(String contentTitle) {
		this.contentTitle = contentTitle;
	}
	public String getGenreName() {
		return genreName;
	}
	public void setGenreName(String genreName) {
		this.genreName = genreName;
	}
	public String getStartDate() {
		return startDate;
	}
	public void setStartDate(String startDate) {
		this.startDate = startDate;
	}
	public String getEndDate() {
		return endDate;
	}
	public void setEndDate(String endDate) {
		this.endDate = endDate;
	}
	public String getContentWriter() {
		return contentWriter;
	}
	public void setContentWriter(String contentWriter) {
		this.contentWriter = contentWriter;
	}
	public String getRecomCount() {
		return recomCount;
	}
	public void setRecomCount(String recomCount) {
		this.recomCount = recomCount;
	}
	
	
}
