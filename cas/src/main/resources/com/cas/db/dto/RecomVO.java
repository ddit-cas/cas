package com.cas.db.dto;

public class RecomVO {

	private String contentNum;
	private String recomDate;
	private String recomMem;
	
	public String getContentNum() {
		return contentNum;
	}
	public void setContentNum(String contentNum) {
		this.contentNum = contentNum;
	}
	public String getRecomDate() {
		return recomDate;
	}
	public void setRecomDate(String recomDate) {
		this.recomDate = recomDate;
	}
	public String getRecomMem() {
		return recomMem;
	}
	public void setRecomMem(String recomMem) {
		this.recomMem = recomMem;
	}
}
