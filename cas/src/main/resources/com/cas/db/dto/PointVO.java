package com.cas.db.dto;


public class PointVO {

	private String chargingNum;
	private int chargingAmount;
	private String chargingDate;
	private String chargingMem;
	
	public String getChargingNum() {
		return chargingNum;
	}
	public void setChargingNum(String chargingNum) {
		this.chargingNum = chargingNum;
	}
	public int getChargingAmount() {
		return chargingAmount;
	}
	public void setChargingAmount(int chargingAmount) {
		this.chargingAmount = chargingAmount;
	}
	public String getChargingDate() {
		return chargingDate;
	}
	public void setChargingDate(String chargingDate) {
		this.chargingDate = chargingDate;
	}
	public String getChargingMem() {
		return chargingMem;
	}
	public void setChargingMem(String chargingMem) {
		this.chargingMem = chargingMem;
	}
}
